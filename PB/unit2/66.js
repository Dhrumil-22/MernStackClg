// Describe the role of the package.json file in a Node.js project and 
// how npm manages dependencies.

// Role of package.json in Node.js:
// It is the main configuration file of a Node.js project.
// It stores project details like name, version, and entry point.
// It keeps track of dependencies and scripts required to run the project.


// How npm manages dependencies:
// npm installs packages using npm install and adds them to package.json.
// It stores installed packages in the node_modules folder.
// It also records exact versions in package-lock.json to ensure consistency.